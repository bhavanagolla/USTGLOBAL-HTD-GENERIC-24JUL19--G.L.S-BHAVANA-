public class AssignmentOperators{
	public static void main(String []args){
	int i;
	int j;

	i=25;
	j=50;

	System.out.println("i = "+i);
	System.out.println("j = "+j);
	
	 j+= i;
	System.out.println("i = "+j);
	j-= i;
	System.out.println("i = "+j);
	 j*= i;
	System.out.println("i = "+j);
	j/= i;
	System.out.println("i = "+j);
	j%= i;
	System.out.println("i = "+j);

	}
}